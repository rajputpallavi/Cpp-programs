#include <iostream>
using namespace std;
int main(){
    int a;
    cout<<"size of integer is "<<sizeof(a)<<endl;
    float b;
    cout<<"size of float is "<<sizeof(b)<<endl;
    char c;
    cout<<"size of character is "<<sizeof(c)<<endl;
    bool d;
    cout<<"size of boolean is "<<sizeof(d)<<endl;
    short int s;
    long int r;
    cout<<"size of short int is "<<sizeof(s)<<endl;
    cout<<"size of long int is "<<sizeof(r)<<endl;
    
    return 0;
}